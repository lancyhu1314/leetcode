package com.suning.fab.loan.bo;

public class CarAccountDetails {
	
	String accDate;		//账务日期
	String brc;			//公司代码
	String acctNo;		//贷款账号
	String customName;	//客户名称
	String chargeType;	//账户类型
	String cdFlag;		//收支标志
	Double tranAmt;		//发生额
	String timeStamp;	//交易时间
	/**
	 * @return the accDate
	 */
	public String getAccDate() {
		return accDate;
	}
	/**
	 * @param accDate the accDate to set
	 */
	public void setAccDate(String accDate) {
		this.accDate = accDate;
	}
	/**
	 * @return the brc
	 */
	public String getBrc() {
		return brc;
	}
	/**
	 * @param brc the brc to set
	 */
	public void setBrc(String brc) {
		this.brc = brc;
	}
	/**
	 * @return the acctNo
	 */
	public String getAcctNo() {
		return acctNo;
	}
	/**
	 * @param acctNo the acctNo to set
	 */
	public void setAcctNo(String acctNo) {
		this.acctNo = acctNo;
	}
	/**
	 * @return the customName
	 */
	public String getCustomName() {
		return customName;
	}
	/**
	 * @param customName the customName to set
	 */
	public void setCustomName(String customName) {
		this.customName = customName;
	}
	/**
	 * @return the chargeType
	 */
	public String getChargeType() {
		return chargeType;
	}
	/**
	 * @param chargeType the chargeType to set
	 */
	public void setChargeType(String chargeType) {
		this.chargeType = chargeType;
	}
	/**
	 * @return the cdFlag
	 */
	public String getCdFlag() {
		return cdFlag;
	}
	/**
	 * @param cdFlag the cdFlag to set
	 */
	public void setCdFlag(String cdFlag) {
		this.cdFlag = cdFlag;
	}
	/**
	 * @return the tranAmt
	 */
	public Double getTranAmt() {
		return tranAmt;
	}
	/**
	 * @param tranAmt the tranAmt to set
	 */
	public void setTranAmt(Double tranAmt) {
		this.tranAmt = tranAmt;
	}
	/**
	 * @return the timeStamp
	 */
	public String getTimeStamp() {
		return timeStamp;
	}
	/**
	 * @param timeStamp the timeStamp to set
	 */
	public void setTimeStamp(String timeStamp) {
		this.timeStamp = timeStamp;
	}

}
